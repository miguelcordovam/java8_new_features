package comparing;

public class Apple {

    private int weight;
    private String country;

    public Apple(int weight, String country) {
        this.weight = weight;
        this.country = country;
    }

    public int getWeight() {
        return weight;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }

    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }


}
